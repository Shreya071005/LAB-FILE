//print pre increment and pre decrement

#include<stdio.h>
int main()
{
    int a = 6;
    printf("the value of a is %d \n",a);
    printf("the value of ++a is %d \n",++a);//pre increment
    printf("the value of --a is %d \n",--a);//pre decrement
    printf("the value of a is %d \n",a);//original value of a
return 0;
}
