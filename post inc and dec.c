//USE OF POST INCREMENT AND POST DECREMENT

#include<stdio.h>
int main()
{
    int a = 8;
    printf("the value of a is %d \n",a);
    printf("the value of a++ is %d \n",a++);//post increment
    //printf("the value of a-- is %d \n",a--);//post decrement
    printf("the value of a is %d \n",a);
    return 0;
}
