//SWAP TWO NUMBER WITHOUT USING THIRD VARIABLE

#include<stdio.h>
int main()
{
    int a , b;
    //BEFORE SWAPPING
    printf("the value of a \n");
    scanf("%d",&a);
    printf("the value of b \n");
    scanf("%d",&b);
    //AFTER SWAPPING
    printf("the value of new a %d \n",b);
    printf("the value of new b %d \n",a);
    return 0;
}
